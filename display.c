void display_digit(char number, char row, char col)
{
	int i,j;
	char x,y;
	unsigned char mask = 0x80;
	x=row;
	y=col;
	for(i=0;i<7;i++) //rows
	{
	   mask = 0x40;
	   printf("\e[%d;%dH",x++,y);
	   for(j=0;j<8;j++) //columns
	   {
	      if(dispData[number][i] & mask)
		     putchar('*');
	      else
		     putchar(0x20);
	         mask>>=1;
	         fflush(stdout);
	   }


	}
}

